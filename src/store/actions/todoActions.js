import { TODO_ADD, TODO_TOGGLE, FILTER_SET } from "./actionConstants";

function doAddTodo(id, name) {
  return {
    type: TODO_ADD,
    todo: { id, name },
  };
}

function doToggleTodo(id) {
  return {
    type: TODO_TOGGLE,
    todo: { id },
  };
}

function doSetFilter(filter) {
  return {
    type: FILTER_SET,
    filter,
  };
}

export { doAddTodo, doToggleTodo, doSetFilter };
